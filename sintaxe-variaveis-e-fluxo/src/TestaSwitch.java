
public class TestaSwitch {
	public static void main(String[] args) {
		
	
	String palavra = "Outra";
	
	switch (palavra) {
		case "Odio":
			System.out.println("palavra errada");
			break;
		case "Amor":
			System.out.println("palavra certa");
			break;
		case "Outra":
			System.out.println("palavra outra");
			break;
		
		}
	
	}

}
